// 1.  Opérateurs égalité / différences

// 1.1. Égalité
// == : vérification de la valeur
console.log('7 == 7 :>> ', 7 == 7); // true
console.log('7 == "7" :>> ', 7 == "7"); // true

// === : vérification de la valeur ET du type
console.log('7 === "7" :>> ', 7 === "7"); // false

// 1.2. Différence
// != : vérification de la valeur
console.log('7 != 7 :>> ', 7 != 7); // false
console.log('7 != "7" :>> ', 7 != "7"); // false

// !== : vérification de la valeur ET du type
console.log('7 !== "7" :>> ', 7 !== "7"); // true